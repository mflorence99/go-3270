func Addr2Bytes(addr uint) []byte {
	chars := make([]byte, 2)
	chars[0] = six2E[(addr>>6)&0b00111111]
	chars[1] = six2E[addr&0b00111111]
	return chars
}

func Bytes2Addr(chars []byte) uint {
	addr := uint(chars[0])
	addr &= 0b00111111
	addr = addr << 6
	addr += uint(chars[1] & 0b00111111)
	return addr
}
